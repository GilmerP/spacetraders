<template>
  <div class="item-card">
    <div class="item-card--header">
      <h4>{{ good.good }}</h4>
    </div>
    <table class="item-card_details">
      <tr>
        <td>Quantity:</td>
        <td>{{ good.quantity }}</td>
      </tr>
      <tr>
        <td>Total Volume:</td>
        <td>{{ good.totalVolume }}</td>
      </tr>
    </table>

    <div class="purchase-container">
      <input v-model.number="quantity" type="number" step="1" />
      <input type="button" value="Sell" @click="handleSell" />
    </div>
  </div>
</template>

<script lang="ts">
import { defineComponent, PropType, ref } from "vue";
import Cargo from "@/interfaces/Cargo";

export default defineComponent({
  emits: ["sellGood"],
  props: {
    good: { type: Object as PropType<Cargo>, required: true }
  },
  setup(props, { emit }) {
    const quantity = ref(props.good.quantity);

    const handleSell = () => {
      emit("sellGood", { good: props.good?.good, quantity: quantity.value });
    };

    return {
      quantity,
      handleSell
    };
  }
});
</script>

<style scoped>
.purchase-container {
  display: flex;
  justify-content: space-evenly;
  margin: 5px 0;
  padding: 5px 0;
  border-top: 1px solid white;
}
input[type="button"] {
  background: red;
}
</style>
